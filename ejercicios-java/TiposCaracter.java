public class TiposCaracter {

    public static void main (String args []){

      char miCaracter = 'a';
      System.out.println("Mi caracter = " + miCaracter);

      char varChar = '\u0021';
      System.out.println("Mi caracter Unicode = " + varChar);

      char varCharDecimal = 33;
      System.out.println("Mi caracter Unicode Decimal = " + varCharDecimal);

      char varCharSimbolo = '!';
      System.out.println("Mi caracter Unicode Simbolo = " + varCharSimbolo);
      
      int letra = 'A';
      System.out.println("Mi caracter Unicode Decimal = " + letra);

    }
    
}
