public class TiposNumericosFlotantes {

    public static void main (String args []){

        float numeroFloat = (float)123456678888.11110;
        System.out.println("Valor de la variable del tipo float: " + numeroFloat);
        System.out.println("Valor minimo del tipo de datos float: " + Float.MIN_VALUE);
        System.out.println("Valor maximo del tipo de datos float: " + Float.MAX_VALUE);
        System.out.println("***");

        Double numeroDoble = (double) 1.7976931348623157E308;
        System.out.println("Valor de la variable del tipo doble: " + numeroDoble);
        System.out.println("Valor minimo del tipo de datos doble: " + Double.MIN_VALUE);
        System.out.println("Valor maximo del tipo de datos doble: " + Double.MAX_VALUE);
        System.out.println("***"); 

    }
    
}
